class EmojiDetailsView @JvmOverloads constructor(
    context: Context,
    attrs: AttributeSet? = null,
    defStyleAttr: Int = 0
) : AbstractComposeView(context, attrs, defStyleAttr) {

    private val emojiDetails = mutableStateMapOf<String, String>()

    @Composable
    override fun Content() {
        EmojiDetails({ emojiDetails })
    }

    fun setEmojiDetails(details: List<EmojiDetail>) {
        emojiDetails.clear()
        details.forEach { (emoji, name) -> emojiDetails[emoji] = name }
    }
}
